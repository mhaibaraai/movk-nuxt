import type { GlobalMeta, z } from 'zod/v4'
import type { IsComponent } from '../core'
import type { AutoFormControl, AutoFormControls } from '../types'
import { isFunction } from '@movk/core'
import defu from 'defu'
import { DEFAULT_CONTROLS } from '../constants/auto-form'
import { joinPath, startCase } from '../core'

interface AutoFormIntrospectedField {
  path: string
  schema: z.ZodType
  originalSchema: z.ZodType
  zodType: string
  meta: GlobalMeta
  decorators: {
    isOptional: boolean
    defaultValue?: any
    description?: string
  }
}

/**
 * 合并默认控制器和自定义控制器
 */
export function mergeControls<TCustom extends AutoFormControls>(custom?: TCustom): TCustom & typeof DEFAULT_CONTROLS {
  return defu(custom, DEFAULT_CONTROLS) as TCustom & typeof DEFAULT_CONTROLS
}

/**
 * 提取schema核心信息，保留装饰器信息
 */
function extractSchemaInfo(schema: z.ZodType): {
  coreSchema: z.ZodType
  decorators: { isOptional: boolean, defaultValue?: any, description?: string }
} {
  let cur: any = schema
  let isOptional = false
  let defaultValue: any
  let description: string | undefined

  while (cur?.def) {
    if (cur.def.type === 'optional') {
      isOptional = true
    }
    if (cur.def.type === 'default') {
      defaultValue = isFunction(cur.def.defaultValue)
        ? cur.def.defaultValue()
        : cur.def.defaultValue
    }
    if (cur.description) {
      description = cur.description
    }
    if (cur.def.innerType) {
      cur = cur.def.innerType
    }
    else {
      break
    }
  }

  return {
    coreSchema: cur as z.ZodType,
    decorators: {
      isOptional,
      defaultValue,
      description,
    },
  }
}

/**
 * 递归地分析 Zod schema，返回一个包含所有字段信息的数组
 */
export function introspectSchema(schema: z.ZodType, parentPath?: string): AutoFormIntrospectedField[] {
  console.log(schema.meta?.())

  const result: AutoFormIntrospectedField[] = []
  const { coreSchema, decorators } = extractSchemaInfo(schema)
  const type = (coreSchema as any).type

  if (type === 'object') {
    const shape = (coreSchema as any).shape
    for (const key of Object.keys(shape)) {
      const child = shape[key] as z.ZodType
      const path = joinPath([...(parentPath ? [parentPath] : []), key])
      result.push(...introspectSchema(child, path))
    }
    return result
  }

  // if (type === 'array') {
  //   const item = (coreSchema as any).element as z.ZodType
  //   const path = joinPath([...(parentPath ? [parentPath] : []), 0])
  //   result.push(...introspectSchema(item, path))
  //   return result
  // }

  const fieldName = parentPath ? parentPath.split('.').pop() || '' : ''
  const autoGeneratedLabel = fieldName ? startCase(fieldName) : ''

  // 获取meta信息
  const originalMeta = (coreSchema.meta?.() || {}) as GlobalMeta
  const finalDescription = originalMeta.description || decorators.description
  const finalLabel = originalMeta.label || autoGeneratedLabel
  const finalRequired = originalMeta.required !== undefined
    ? originalMeta.required
    : !decorators.isOptional

  result.push({
    path: parentPath ?? '',
    schema: coreSchema,
    originalSchema: schema,
    zodType: type,
    meta: {
      ...originalMeta,
      label: finalLabel,
      description: finalDescription,
      required: finalRequired,
    },
    decorators,
  })
  return result
}

